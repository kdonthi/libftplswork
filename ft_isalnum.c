int	ft_isalnum(int c)
{
	unsigned char x;
	
	if (c >= 256 || c < 0)
		return (0);
	x = (unsigned char)c;
	if ((x >= '0' && x <= '9') || (x >= 'a' && x <= 'z') ||
	(x >= 'A' && x <= 'Z'))
		return (1);
	else
		return (0);
}
