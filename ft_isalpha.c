#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char x;

	x = (unsigned char)c;
	if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z'))
		return (1);
	else
		return (0);
}
