#include <string.h>

void	ft_memset(void *s, int c, size_t n)
{
	unsigned char	*x;
	size_t			i;

	x = s;
	i = 0;
	while (i < n)
	{
		*x = (unsigned char)c;
		x++;
		i++;
	}
}
